#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*buffer;
	int		i;

	buffer = malloc(sizeof(char) * (ft_strlen(s1) + 1));
	if (!buffer)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		buffer[i] = s1[i];
		i++;
	}
	buffer[i] = '\0';
	return (buffer);
}
